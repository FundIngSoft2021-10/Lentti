    public void MostrarRestaurantes(){
        
        restaurantes.clear();
        ArrayList<String> rest = new ArrayList<>();
        
        rest.add("Holi");
        rest.add("Prueba2");
        
        for( String i: rest )
            restaurantes.addElement(i);
        
        jList1.setModel(restaurantes); //Muestra los restaurantes que coinciden en la busqueda
    }
    
