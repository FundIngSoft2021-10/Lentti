    private void jButtonCambiarContraActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonCambiarContraActionPerformed
        // TODO add your handling code here:
        if (this.jPasswordFieldContrasena.getText().isEmpty())
        {
            JOptionPane.showMessageDialog(null, "Campo contraseña vacío");
        }
        else
        {
            consultasBaseDeDatos consulta=new BaseDeDatos();
            consulta.ModificarContrasena(pUsuario, this.jPasswordFieldContrasena.getText());
            JOptionPane.showMessageDialog(null, "Se cambio la contraseña");
            InicioSesion iniciador = new InicioSesion();
            iniciador.setVisible(true);
            this.dispose();
        }
    }//GEN-LAST:event_jButtonCambiarContraActionPerformed

