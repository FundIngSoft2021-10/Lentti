    private void jButtonAceptarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonAceptarActionPerformed
        consultasBaseDeDatos consulta = new BaseDeDatos();
        boolean resultado = false;
        
        if(this.jTextFieldNombreRestaurante.getText().isEmpty())
        {
            JOptionPane.showMessageDialog(null, "No se ha indicado un nombre.");
        }
        else if (!consulta.ValidarExistenciaRestaurante(this.jTextFieldNombreRestaurante.getText()))
        {
            JOptionPane.showMessageDialog(null, "El restaurante no existe.");
            AgregarRestauranteFavorito pantallaGestion = new AgregarRestauranteFavorito(usuario);
            pantallaGestion.setVisible(true);
            this.dispose();
        }
        else
        {
            resultado = consulta.AgregarRestauranteFavorito(usuario, this.jTextFieldNombreRestaurante.getText());
            JOptionPane.showMessageDialog(null, "Restaurante agregado exitosamente.");
            ListaDeRestaurantesFavoritos pantallaGestion = new ListaDeRestaurantesFavoritos(usuario);
            pantallaGestion.setVisible(true);
            this.dispose();
        }
        
        
    }//GEN-LAST:event_jButtonAceptarActionPerformed
       
    
