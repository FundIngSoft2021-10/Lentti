    private void jButtonEliminarMiCuentaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonEliminarMiCuentaActionPerformed
        // TODO add your handling code here:
        boolean resultado=false;
        consultasBaseDeDatos consulta = new BaseDeDatos();
        resultado=consulta.EliminarCuenta(usuario,"A");
        if(resultado)
        {
            JOptionPane.showMessageDialog(null, "La Cuenta ha sido eliminada.");
            InicioSesion iniciador = new InicioSesion();
            iniciador.setVisible(true);
            this.dispose();
            
        }
        else
        {
            JOptionPane.showMessageDialog(null, "La Cuenta no pudo ser borrada");
        }
    }//GEN-LAST:event_jButtonEliminarMiCuentaActionPerformed

    private void jButtonEliminarCuentaSeleccionadaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonEliminarCuentaSeleccionadaActionPerformed
        // TODO add your handling code here:
        boolean resultado=false;
        String eliminar="";
        consultasBaseDeDatos consulta = new BaseDeDatos();
        eliminar= this.jListListaCuentas.getSelectedValue();
        resultado= consulta.EliminarCuenta(eliminar,"A");
        if(this.jListListaCuentas.getSelectedIndex() == -1)
        {
             JOptionPane.showMessageDialog(null, "No ha seleccionado algun usuario");
        }
        else if(resultado)
        {
            JOptionPane.showMessageDialog(null, "La Cuenta ha sido borrada");
            this.listaUsuarios= consulta.LlenarListaUsuarios("A",usuario);
            this.jListListaCuentas.setModel(listaUsuarios);
            
        }
        else
        {
            JOptionPane.showMessageDialog(null, "La Cuenta no ha sido borrada");
        }
        
        
        
    }//GEN-LAST:event_jButtonEliminarCuentaSeleccionadaActionPerformed

