    private void BotonModificarNombreClienteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_BotonModificarNombreClienteActionPerformed
        // TODO add your handling code here:
        
        consultasBaseDeDatos consulta = new BaseDeDatos();
        if(consulta.ModificarNombreCliente(LabelNombreUsuario.getText(), TF_ModificarNombreCliente.getText()))
        {
                    JOptionPane.showMessageDialog(null, "Se modifico el nombre del cliente con exito");
        }
        else
        {
            TF_ModificarNombreCliente.setText("");
            JOptionPane.showMessageDialog(null, "Error al Modificar el nombre del cliente");
        }
        
        
        
        
    }//GEN-LAST:event_BotonModificarNombreClienteActionPerformed

