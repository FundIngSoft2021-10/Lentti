    private void jButtonEstadoPedidoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonEstadoPedidoActionPerformed

       consultasBaseDeDatos consulta = new BaseDeDatos();
        String doc = consulta.DarDomiciliarioDoc(cliente);
        int id = Integer.parseInt(this.jListPedidosEnCurso.getSelectedValue().toString());
        if (this.jListPedidosEnCurso.getSelectedValue() == null) {
            JOptionPane.showMessageDialog(null, "Por favor seleccione un pedido");
        } else {
            JOptionPane.showMessageDialog(null, "Usted entregar√° este pedido");
            consulta.RelacionarPedidoDomiciliario(id,doc);
            PantallaPedidosEnCursoDomiciliario p = new PantallaPedidosEnCursoDomiciliario(cliente);
            p.setVisible(true);
            this.dispose();
        }
    }//GEN-LAST:event_jButtonEstadoPedidoActionPerformed

